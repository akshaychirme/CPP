/*
Operators:-
    Operator is a symbol that is used to perform mathematical and logical task.

    1) Arithmatic Operator (+,-,*,%,/)
    2) Relational Operator (<,>,<=,>=,==,!=)
    3) Logical Operator (&&, ||, !)
    4) Assignment Operator (=, +=, -=, *=, /=)
    5) Ternary Operator (? :)  -->if-else
    6) Bitwise Operator (&, |, ^, ~, <<, >>)

• Increment and Decrement Operator:-
    ♦ Increment operator:-
        Increment operator is used to increse the value of variable by 1.
        Types -->
            1) Pre-increment  (++a)
            2) Post-increment (a++)
    
    ♦ Decrement operator:-
        Decrement operator is used to decrese the value of variable by 1.
        Types -->
            1) Pre-decrement  (--a)
            2) Post-decrement (a--)
*/

